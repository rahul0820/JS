// {} it is called a object literal

let person = {
    name: 'Rahul',
    age : 10

};

console.log(person);


// if we want to update name then there are 2 ways
// DOT notation
person.name = 'john';
console.log(person.name);

//Bracket Notation
person['name'] = 'cherry';
console.log(person.name);

